function [dHxdxi,dHxdyi, dHydxi, dHydyi] = DKT_dH(p_k, q_k, r_k, t_k, xi, yi)
  
    dHxdxi = [...
        p6*(1-2*xi) + (p5-p6)*yi,...
        q6*(1-2*xi)-(q5+q6)*yi,...
        -4 + 6*(xi+yi) + r6*(1-2*xi) - (r5+r6)*yi,...
        -p6*(1-2*xi) + (p4+p6)*yi,...
        q6*(1-2*xi) - (q6-q4)*yi,...
        -2 + 6*xi + r6*(1-2*xi) + (r4-r6)*yi,...
        -yi*(p5+p4),...
        yi*(q4-q5),...
        -yi*(r5-r4)...
        ];
            
                   
    dHydxi = [...
        t6*(1-2*xi) + (t5-t6)*yi,...
        1 + r6*(1-2*xi) - (r5+r6)*yi,...
        -q6*(1-2*xi) - (q5+q6)*yi,...
        -t6*(1-2*xi) + (t4+t6)*yi,...
        -1 + r6*(1-2*xi) - (r4-r6)*yi,...
        q6*(1-2*xi) + (q4-q6)*yi,...
        -(t4+t5)*yi,...
        (r4-r5)*yi,...
        -(q4-q5)*yi...
        ];
        
    dHxdyi = [...
        -p5*(1-2*xi) - (p6-p5)*xi,...
        q5*(1-2*yi) - (q5+q6)*xi,...
        -4 + 6*(xi+yi) + r5*(1-2*yi) - (r5+r6)*xi,...
        (p4+p6)*xi,...
        (q4-q6)*xi,...
        -(r6-r4)*xi,...
        p5*(1-2*yi) - (p4+p5)*xi,...
        q5(1-2*yi) + (q4-q5)*xi,...
        -2 + 6*yi + r5*(1-2*yi) + (r4-r5)*xi...
        ];

    dHydyi = [...
        -t5*(1-2*yi) - (t6-t5)*xi,...
        1 + r5*(1-2*yi) - (r5+r6)*xi,...
        -q5*(1-2*yi) + (q5+q6)*xi,...
        xi*(t4+t6),...
        xi*(r4-r6),...
        -xi*(q4-q6),...
        t5*(1-2*yi) - (t4+t5)*xi,...
        1 - r5*(1-2*yi) + (r4-r5)*xi,...
        -q5*(1-2*yi) + (q4-q5)*xi...
        ];
        
        
    
end